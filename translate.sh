#!/bin/bash
cd /opt/Marzbot-free

# proxy.py - handlers/user
FILE=app/handlers/user/proxy.py
sed -i 's/🔵 لیست پروکسی‌های خریداری شده👇 (برای مدیریت هر پروکسی روی آن کلیک کنید)/🔵 Список ваших прокси 👇 (нажмите для управления)/' $FILE
sed -i 's/❌ پروکسی در سرور یافت نشد!/❌ Прокси не найден на сервере!/' $FILE
sed -i 's/شناسه: {proxy.username}/ID: {proxy.username}/' $FILE
sed -i 's/⭐️ شناسه:/⭐️ ID:/' $FILE
sed -i 's/🌀 وضعیت:/🌀 Статус:/' $FILE
sed -i 's/⏳ تاریخ انقضا:/⏳ Истекает:/' $FILE
sed -i 's/📊 حجم مصرف شده:/📊 Использовано:/' $FILE
sed -i 's/🔋 حجم باقی‌مانده:/🔋 Осталось:/' $FILE
sed -i 's/🔑 پروکسی های فعال:/🔑 Активные прокси:/' $FILE
sed -i 's/🔗 لینک اتصال هوشمند:/🔗 Ссылка подключения:/' $FILE
sed -i 's/❕برای اطلاع یافتن از وضعیت پروکسی بدون وارد شدن به ربات، میتونید لینک اتصال هوشمند رو ذخیره کنید و در مروگر باز کنید، یا اینکه روی لینک زیر کلیک کنید:/❕ Сохраните ссылку для проверки статуса без бота, или нажмите:/' $FILE
sed -i 's/💡 برای دریافت 📝 Инструкцияی اتصال و استفاده دستور را ارسال کنید!/💡 Для инструкции отправьте \/help/' $FILE
sed -i 's/💡 برای قطع اتصال افراد متصل می‌توانید از دکمه «تغییر پسوورد» استفاده کنید!/💡 Для отключения пользователей нажмите «Сменить пароль»/' $FILE
sed -i 's/💡 برای دریافت لینک‌های اتصال و Qr Code میتوانید از دکمه زیر استفاده کنید👇/💡 Для получения ссылок и QR-кода нажмите кнопку ниже 👇/' $FILE
sed -i 's/⚠️ مطمئن هستید که میخواهید سرویس مورد نظر را از لیست پروکسی‌های خود حذف کنید؟ پس از حذف امکان تمدید وجود نخواهد داشت!/⚠️ Вы уверены, что хотите удалить подписку? Восстановление невозможно!/' $FILE
sed -i 's/✅ اشتراک از لیست پروکسی‌های شما حذف شد/✅ Подписка удалена/' $FILE
sed -i 's/💡 در این بخش می‌توانید دسترسی افراد متصل را قطع کنید!/💡 Здесь можно отключить подключённых пользователей!/' $FILE
sed -i 's/برای انجام این کار دو روش دارید:/Два способа:/' $FILE
sed -i "s/1️⃣ تغییر پسوورد: فقط پسوورد کانفیگ‌ها عوض شده و کاربر با استفاده از لینک اتصال هوشمند می‌تواند دوباره متصل شود./1️⃣ Сменить пароль: меняет только пароль, пользователь может переподключиться по ссылке/" $FILE
sed -i 's/2️⃣ تغییر اتصال هوشمند: لینک اتصال هوشمند کاربر را تغییر می‌دهد و کاربر توانایی آپدیت و استفاده از لینک اتصال هوشمند قدیمی را نخواهد داشت./2️⃣ Сменить ссылку: меняет ссылку подключения, старая перестанет работать/' $FILE
sed -i 's/✅ پسوورد پروکسی تغییر یافت/✅ Пароль изменён/' $FILE
sed -i 's/⚠️ مطمئن هستید که میخواهید لینک اتصال هوشمند سرویس مورد نظر تغییر کند؟ امکان استفاده از لینک اتصال هوشمند قدیمی وجود نخواهد داشت!/⚠️ Старая ссылка подключения перестанет работать! Продолжить?/' $FILE
sed -i 's/✅ لینک اتصال هوشمند تغییر یافت/✅ Ссылка подключения изменена/' $FILE
sed -i 's/💡 برای کپی کردن هرکدام از لینک‌ها روی آن کلیک کنید👆/💡 Нажмите на ссылку чтобы скопировать 👆/' $FILE
sed -i 's/💡 برای دریافت <b>Qr code<\/b> از دکمه‌های زیر استفاده کنید👇/💡 Для получения QR-кода нажмите кнопку ниже 👇/' $FILE
sed -i 's/⛓️ لینک Qr code اتصال هوشمند/⛓️ QR-код подключения/' $FILE
sed -i 's/❗️برای اشتراک مورد نظر امکان تمدید وجود ندارد!/❗️ Продление недоступно для этой подписки/' $FILE
sed -i 's/برای تمدید اشتراک میتونید یکی از سرویس‌های زیر رو выберите 👇:/Для продления выберите тариф 👇:/' $FILE
sed -i 's/✅ برای تمدید میتونید یکی از حالت‌های زیر رو выберите 👇👇/✅ Выберите способ продления 👇👇/' $FILE
sed -i 's/➖ تمدید آنی: دوره جدید اشتراک شما از همین لحظه محاسبه می‌شود و سرویس جدید برای شما فعال می‌شود./➖ Мгновенное: новый период начинается сейчас/' $FILE
sed -i 's/➖ رزور پلن پشتیبان: پس از اتمام حجم یا دوره اشتراک فعلی، سرویس جدید به طور خودکار فعال می‌شود./➖ Резерв: активируется после окончания текущего/' $FILE
sed -i 's/🌀 آیا مایل به فعال سازی سرویس زیر برای این پروکسی هستید؟/🌀 Активировать тариф для этого прокси?/' $FILE
sed -i 's/🖥 حجم:/🖥 Трафик:/' $FILE
sed -i 's/🛍 برای تمدید آنی و فعالسازی سرویس، دکمه زیر را کلیک کنید👇/🛍 Для активации нажмите кнопку 👇/' $FILE
sed -i 's/😞 موجودی حساب شما برای فعالسازی این سرویس کافی نیست! برای افزایش اعتبار دکمه زیر را کلیک کنید👇/😞 Недостаточно средств! Пополните баланс 👇/' $FILE
sed -i "s/lang=\"fa\"/lang=\"ru\"/g" $FILE

# base.py
sed -i 's/برای رفتن به منوی اصلی دستور \/menu رو ارسال کنید😉/Для возврата в главное меню отправьте \/menu 😉/' app/handlers/base.py

# admin/user.py
sed -i 's/تعداد سرویس‌های تست شما تنظیم نشده است، بنابراین امکان تنظیم برای کاربران خود را ندارید! Пожалуйста، свяжитесь с поддержкой./Лимит тестовых сервисов не настроен! Свяжитесь с поддержкой./' app/handlers/admin/user.py
sed -i 's/💡 این متن در ابتدای نام پروکسی‌های شما قرار می‌گیرد و فقط میتواند شامل حروف انگلیسی یا اعداد باشد!/💡 Этот текст добавляется в начало имени прокси. Только английские буквы и цифры!/' app/handlers/admin/user.py
sed -i 's/✍️ پیشوند پروکسی را برای تنظیم وارد کنید:/✍️ Введите префикс для прокси:/' app/handlers/admin/user.py
sed -i 's/❌ پیشوند پروکسی‌ها فقط می‌تواند شامل اعداد و حروف انگلیسی باشد! دوباره ارسال کنید:/❌ Префикс может содержать только английские буквы и цифры!/' app/handlers/admin/user.py
sed -i 's/❌ پیشوند پروکسی‌ها فقط می‌تواند بین ۴ تا ۲۰ کاراکتر باشد! دوباره ارسال کنید:/❌ Префикс должен быть от 4 до 20 символов!/' app/handlers/admin/user.py
sed -i 's/برای {type} باید روی یک پیام ریپلای کنید!/Для {type} нужно ответить на сообщение!/' app/handlers/admin/admin.py

# keyboards
sed -i 's/پروکسی‌ها/Прокси/' app/keyboards/admin/user.py
sed -i 's/🔗 دریافت لینک‌های اتصال/🔗 Получить ссылки/' app/keyboards/user/proxy.py

echo "✅ Готово! Пересобираем..."
docker compose down && docker compose up -d --build
